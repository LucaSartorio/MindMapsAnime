import type { Route, RouteStep } from '@/types';

/**
 * Rotte aggiuntive:
 *  - i nove percorsi della dispersione (i due anni di allenamento di ciascun
 *    Cappello di Paglia, scagliato da Kuma da Sabaody alla sua isola).
 *  - le rotte delle altre Supernove della Peggiore Generazione nel New World.
 * Ogni rotta parte dall'Arcipelago Sabaody (loc-op-sabaody).
 */

const SABAODY: RouteStep = {
  order: 1,
  locationId: 'loc-op-sabaody',
  title: { it: 'Sabaody — la dispersione', en: 'Sabaody — the scattering' },
};

/** Raggruppamenti tematici dei percorsi (mostrati come sezioni nel pannello). */
const GROUP_TIMESKIP = { it: 'Ciurma divisa (2 anni)', en: 'Crew split (2 years)' } as const;
const GROUP_SUPERNOVA = { it: 'Supernove', en: 'Supernovas' } as const;

/** Rotta semplice a 2 tappe: Sabaody → destinazione. */
function trip(
  id: string,
  nameIt: string,
  nameEn: string,
  descIt: string,
  descEn: string,
  protagonist: string,
  destLoc: string,
  destTitle: { it: string; en: string },
  color: string,
  lineStyle: 'dashed' | 'dotted',
  manga: string[],
  anime: string[],
  related: string[] = [],
  tags: string[] = [],
): Route {
  return {
    id,
    worldId: 'world-onepiece',
    type: 'character',
    name: nameIt,
    localizedName: { it: nameIt, en: nameEn },
    group: lineStyle === 'dotted' ? GROUP_TIMESKIP : GROUP_SUPERNOVA,
    description: { it: descIt, en: descEn },
    protagonistCharacterIds: [protagonist],
    ...(related.length ? { relatedCharacterIds: related } : {}),
    color,
    lineStyle,
    mangaChapters: manga,
    animeEpisodes: anime,
    canonStatus: 'canon',
    referenceStatus: 'verified',
    tags,
    steps: [SABAODY, { order: 2, locationId: destLoc, title: destTitle }],
  };
}

